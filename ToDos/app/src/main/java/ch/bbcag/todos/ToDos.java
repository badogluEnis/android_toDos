package ch.bbcag.todos;


public final class ToDos {
    private String TODOS_ID = "id";
    private String TODOS_TITLE = "title";
    private String TODOS_DESCRIPTION = "description";
    private String TODOS_DATE = "date";
    private String TODO_PUSHMESSAGE = "pushmessage";
    private String TODOS_ISOPEN = "isopen";


    public String getTODOS_ID() {
        return TODOS_ID;
    }

    public void setTODOS_ID(String TODOS_ID) {
        this.TODOS_ID = TODOS_ID;
    }

    public String getTODOS_TITLE() {
        return TODOS_TITLE;
    }

    public void setTODOS_TITLE(String TODOS_TITLE) {
        this.TODOS_TITLE = TODOS_TITLE;
    }

    public String getTODOS_DESCRIPTION() {
        return TODOS_DESCRIPTION;
    }

    public void setTODOS_DESCRIPTION(String TODOS_DESCRIPTION) {
        this.TODOS_DESCRIPTION = TODOS_DESCRIPTION;
    }

    public String getTODOS_DATE() {
        return TODOS_DATE;
    }

    public void setTODOS_DATE(String TODOS_DATE) {
        this.TODOS_DATE = TODOS_DATE;
    }

    public String getTODO_PUSHMESSAGE() {
        return TODO_PUSHMESSAGE;
    }

    public void setTODO_PUSHMESSAGE(String TODO_PUSHMESSAGE) {
        this.TODO_PUSHMESSAGE = TODO_PUSHMESSAGE;
    }

    public String getTODOS_ISOPEN() {
        return TODOS_ISOPEN;
    }

    public void setTODOS_ISOPEN(String TODOS_ISOPEN) {
        this.TODOS_ISOPEN = TODOS_ISOPEN;
    }

}

